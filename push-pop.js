
var intArr = [23, 45];
intArr.push(67); // returns the new array length
console.log(intArr);


var intArr = [23, 45, 67];
intArr.pop(); // returns the removed item
console.log(intArr);

let arr = [2, 3, 4];
arr.unshift(1); // adds 1 to the beginning of the array
console.log(arr); // outputs [1, 2, 3, 4]

let arrr = [1, 2, 3, 4];
arr.shift(); // removes the first element from the array
console.log(arrr); // outputs [2, 3, 4]
